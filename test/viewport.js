var viewport = require('../'),
    test = require('tap').test;

test('viewport', function(t) {
    t.deepEqual(viewport.viewport([
        5.668343999999995,
        45.111511000000014,
        5.852471999999996,
        45.26800200000002
    ], [640, 480]), {
        center: [
            5.7604079999999955,
            45.189756500000016
        ],
        zoom: 5
    });
    t.deepEqual(viewport.viewport([
        5.668343999999995,
        45.111511000000014,
        5.852471999999996,
        45.26800200000002
    ], [6400, 4800]), {
        center: [
            5.7604079999999955,
            45.189756500000016
        ],
        zoom: 3
    });
    t.deepEqual(viewport.viewport([
        5.668343999999995,
        45.111511000000014,
        5.852471999999996,
        45.26800200000002
    ], [64, 48]), {
        center: [
            5.7604079999999955,
            45.189756500000016
        ],
        zoom: 15
    });
    t.end();
});
